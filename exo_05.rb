# # {} il permet de mettre du code ruby dans une chaîne, il est généralement utilisé pour insérer des valeurs de variable dans des chaînes sans avoir à se soucier de la concaténation de chaînes

# c'est une chaine de caratere
puts "On va compter le nombre d'heures de travail à THP"


# c'est le nombre d'heure de travail effectuer sur 11 semaine
puts "Travail : #{10 * 5 * 11}"

# c'est le nombre de minute effectuer sur 11 semaine
puts "En minutes ça fait : #{10 * 5 * 11 * 60}"

#c'est une chaine de caractere
puts "Et en secondes ?"

#c'est le nombre de seconde effectuer sur 11 semaine
puts 10 * 5 * 11 * 60 * 60


# c'est une chaine de caractere
puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?"
 
#c'est une operande de type boolean qui envoie true or false
puts 3 + 2 < 5 - 7

#resultat de 3+2
puts "Ça fait combien 3 + 2 ? #{3 + 2}"

# resultat de 5-7
puts "Ça fait combien 5 - 7 ? #{5 - 7}"

#c'est une chaine de caractere
puts "Ok, c'est faux alors !"

#c'est une chaine de caractere suivie d'une expression litteral de type boolean
puts "C'est drôle ça, faisons-en plus :"

#c'est une chaine de caractere suivie d'une expression litteral de type boolean
puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}"

#c'est une chaine de caractere suivie d'une expression litteral de type boolean
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}"